DROP VIEW IF EXISTS "TPCH_SF1_DFV"."VW_PARTSUPP";
CREATE  VIEW "TPCH_SF1_DFV"."VW_PARTSUPP"  AS 
	WITH "DELTA_WINDOW" AS 
	( 
		SELECT 
			  "LWT_SRC"."FMC_BEGIN_LW_TIMESTAMP" AS "FMC_BEGIN_LW_TIMESTAMP"
			, "LWT_SRC"."FMC_END_LW_TIMESTAMP" AS "FMC_END_LW_TIMESTAMP"
		FROM "TPCH_SF1_MTD"."FMC_LOADING_WINDOW_TABLE" "LWT_SRC"
	)
	, "DELTA_VIEW_FILTER" AS 
	( 
		SELECT 
			  "CDC_SRC"."CDC_TIMESTAMP" AS "CDC_TIMESTAMP"
			, 'S' ::varchar AS "RECORD_TYPE"
			, "CDC_SRC"."PS_PARTKEY" AS "PS_PARTKEY"
			, "CDC_SRC"."PS_SUPPKEY" AS "PS_SUPPKEY"
			, "CDC_SRC"."PS_AVAILQTY" AS "PS_AVAILQTY"
			, "CDC_SRC"."PS_SUPPLYCOST" AS "PS_SUPPLYCOST"
			, "CDC_SRC"."PS_COMMENT" AS "PS_COMMENT"
			, "CDC_SRC"."LOAD_TIMESTAMP" AS "LOAD_TIMESTAMP"
		FROM "TPCH_SF1_CDC"."CDC_PARTSUPP" "CDC_SRC"
		INNER JOIN "DELTA_WINDOW" "DELTA_WINDOW" ON  1 = 1
		WHERE  "CDC_SRC"."CDC_TIMESTAMP" > "DELTA_WINDOW"."FMC_BEGIN_LW_TIMESTAMP" AND "CDC_SRC"."CDC_TIMESTAMP" <= "DELTA_WINDOW"."FMC_END_LW_TIMESTAMP"
	)
	, "DELTA_VIEW" AS 
	( 
		SELECT 
			  "DELTA_VIEW_FILTER"."CDC_TIMESTAMP" AS "CDC_TIMESTAMP"
			, "DELTA_VIEW_FILTER"."RECORD_TYPE" AS "RECORD_TYPE"
			, "DELTA_VIEW_FILTER"."PS_PARTKEY" AS "PS_PARTKEY"
			, "DELTA_VIEW_FILTER"."PS_SUPPKEY" AS "PS_SUPPKEY"
			, "DELTA_VIEW_FILTER"."PS_AVAILQTY" AS "PS_AVAILQTY"
			, "DELTA_VIEW_FILTER"."PS_SUPPLYCOST" AS "PS_SUPPLYCOST"
			, "DELTA_VIEW_FILTER"."PS_COMMENT" AS "PS_COMMENT"
			, "DELTA_VIEW_FILTER"."LOAD_TIMESTAMP" AS "LOAD_TIMESTAMP"
		FROM "DELTA_VIEW_FILTER" "DELTA_VIEW_FILTER"
	)
	, "PREPJOINBK" AS 
	( 
		SELECT 
			  "DELTA_VIEW"."CDC_TIMESTAMP" AS "CDC_TIMESTAMP"
			, "DELTA_VIEW"."RECORD_TYPE" AS "RECORD_TYPE"
			, COALESCE("DELTA_VIEW"."PS_PARTKEY", CAST("MEX_BK_SRC"."KEY_ATTRIBUTE_INTEGER" AS INTEGER)) AS "PS_PARTKEY"
			, COALESCE("DELTA_VIEW"."PS_SUPPKEY", CAST("MEX_BK_SRC"."KEY_ATTRIBUTE_INTEGER" AS INTEGER)) AS "PS_SUPPKEY"
			, "DELTA_VIEW"."PS_AVAILQTY" AS "PS_AVAILQTY"
			, "DELTA_VIEW"."PS_SUPPLYCOST" AS "PS_SUPPLYCOST"
			, "DELTA_VIEW"."PS_COMMENT" AS "PS_COMMENT"
			, "DELTA_VIEW"."LOAD_TIMESTAMP" AS "LOAD_TIMESTAMP"
		FROM "DELTA_VIEW" "DELTA_VIEW"
		INNER JOIN "TPCH_SF1_MTD"."MTD_EXCEPTION_RECORDS" "MEX_BK_SRC" ON  1 = 1
		WHERE  "MEX_BK_SRC"."RECORD_TYPE" = 'N'
	)
	SELECT 
		  "PREPJOINBK"."CDC_TIMESTAMP" AS "CDC_TIMESTAMP"
		, "PREPJOINBK"."RECORD_TYPE" AS "RECORD_TYPE"
		, "PREPJOINBK"."PS_PARTKEY" AS "PS_PARTKEY"
		, "PREPJOINBK"."PS_SUPPKEY" AS "PS_SUPPKEY"
		, "PREPJOINBK"."PS_AVAILQTY" AS "PS_AVAILQTY"
		, "PREPJOINBK"."PS_SUPPLYCOST" AS "PS_SUPPLYCOST"
		, "PREPJOINBK"."PS_COMMENT" AS "PS_COMMENT"
		, "PREPJOINBK"."LOAD_TIMESTAMP" AS "LOAD_TIMESTAMP"
	FROM "PREPJOINBK" "PREPJOINBK"
	;

 
 
